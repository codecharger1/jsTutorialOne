// switch statement which gives instruction according to the weather
// day. 

let weather = "thys";


switch (weather){
    case "sunny":
      console.log("Wear sunglasses");
      break;
    case "rainy":
      console.log("take umbrella"); 
      break; 
    case "cold":
      console.log("Wear a coat and gloves.");
      break;
    default:
      console.log("Unknown weather.");  
}