// Math.floor()

console.log(Math.floor(5.7));


// Math.ceil()

console.log(Math.ceil(5.2));

// Math.round()

console.log(Math.round(6.7));

// Math.sqrt()

console.log(Math.sqrt(16));


// Math.abs()


console.log(Math.abs(-10));


